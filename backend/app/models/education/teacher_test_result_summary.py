from sqlalchemy import Column, Integer, String, DateTime, Boolean, Text
from app.db import Base

class TeacherTestResultSummary(Base):
    __tablename__ = "teacher_test_result_summary"
    id = Column(Integer, primary_key=True, index=True)
    teacher_id = Column(Integer, index=True, comment="ID giáo viên")
    teacher_name = Column(String, comment="Tên giáo viên")
    update_datetime = Column(DateTime, comment="更新日時 (Update Date and Time)")
    test = Column(String, comment="テスト (Test)")
    subject = Column(String, comment="教科・科目 (Subject)")
    implementation_date = Column(DateTime, comment="実施日 (Implementation Date)")
    answer = Column(String, comment="答案 (Answer)")
    answer_status = Column(Boolean, comment="Answer status")
    problem_attribute = Column(Text, comment="問題属性 (Problem Attribute)")
    problem_attribute_status = Column(Boolean, comment="Problem Attribute status")
    individual_ticket = Column(String, comment="個票 (Individual Ticket)")
    individual_ticket_status = Column(Boolean, comment="Individual Ticket status")
    review = Column(String, comment="振り返り (Review) - deadline info")
    review_setting_status = Column(Boolean, comment="Review setting Status")
    review_response = Column(String, comment="Review response (số người đã phản hồi)")
    review_response_unanswered = Column(String, comment="Review response (số người chưa phản hồi)")
    lrt = Column(String, comment="LRT (tool or metric)")
    lrt_status = Column(Boolean, comment="LRT status")
    realtendant_tool = Column(String, comment="Realtendantツール (Realtendant Tool)")
    realtendant_tool_status = Column(Boolean, comment="Realtendantツール status")
    school_year = Column(Integer, comment="学年 (Năm)")
    school_name = Column(String, comment="Tên trường học")
    school_id = Column(Integer, comment="ID trường học")
    average_score = Column(Integer, comment="得点 (Điểm trung bình)")
    correct_answer_rate_total = Column(String, comment="正答率（％）- 全体")
    correct_answer_rate_knowledge = Column(String, comment="正答率（％）- 知識・技能")
    correct_answer_rate_thinking = Column(String, comment="正答率（％）- 思考・判断・表現")
    careless_mistake_count = Column(Integer, comment="ケアレスミスかも (Số câu có thể là lỗi bất cẩn)")
    deviation_value = Column(String, comment="偏差値")
    understanding_rank = Column(String, comment="理解度ランク")
